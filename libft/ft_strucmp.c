#include "libft.h"

int	ft_strucmp(const char *s1, const char *s2)
{
	while ((*s1 != '\0' && *s1 == *s2) || (*s1 != '\0' && *s1 == (*s2 + 32))
		|| (*s1 != '\0' && *s1 == (*s2 - 32)))
	{
		s1++;
		s2++;
	}
	if (*s1 == '\0' && *s2 == '\0')
	{
		return (1);
	}
	return (0);
}
