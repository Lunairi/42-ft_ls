#include "libft.h"

char	*ft_strndup(const char *s1, size_t n)
{
	char	*copy;

	copy = (char*)malloc(n + 1);
	if (copy)
	{
		ft_strncpy(copy, s1, n);
		copy[n] = '\0';
	}
	return (copy);
}
