#include "libft.h"

void	ft_putoct(unsigned int n, int *p)
{
	if (n > 7)
	{
		ft_putoct((n / 8), p);
		ft_putoct((n % 8), p);
	}
	else
	{
		*p = *p + 1;
		ft_putchar(n + '0');
	}
}
